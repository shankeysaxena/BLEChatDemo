import UIKit

extension UIColor {
    //MARK:- Default color property
    static var defaultColor: UIColor {
        return UIColor(red: 235/255, green: 235/255, blue: 235/255, alpha: 1.0)
    }
    //MARK:- Default color for chat bubble
    static var sentBubbleColor: UIColor {
        return UIColor(red: 0, green: 137/255, blue: 249/255, alpha: 1.0)
    }
    
}

